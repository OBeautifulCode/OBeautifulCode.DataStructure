namespace OBeautifulCode.DataStructure
{
    /// <summary>
    /// Specifies the style of a fill pattern.
    /// </summary>
    public enum FillPatternStyle
    {
        /// <summary>
        /// Unknown (default).
        /// </summary>
        Unknown,

        /// <summary>
        /// 6.25% grayed-out pattern.
        /// </summary>
        GrayedOutSixAndOneQuarterPercent,

        /// <summary>
        /// 12.5% grayed-out pattern.
        /// </summary>
        GrayedOutTwelveAndOneHalfPercent,

        /// <summary>
        /// 25% grayed-out pattern.
        /// </summary>
        GrayedOutTwentyFivePercent,

        /// <summary>
        /// 50% grayed-out pattern.
        /// </summary>
        GrayedOutFiftyPercent,

        /// <summary>
        /// 75% grayed-out pattern.
        /// </summary>
        GrayedOutSeventyFivePercent,

        /// <summary>
        /// A diagonal crosshatch pattern.
        /// </summary>
        DiagonalCrosshatch,

        /// <summary>
        /// A thin vertical stripe pattern.
        /// </summary>
        ThinVerticalStripe,

        /// <summary>
        /// A vertical stripe pattern.
        /// </summary>
        VerticalStripe,

        /// <summary>
        /// A thin horizontal stripe pattern.
        /// </summary>
        ThinHorizontalStripe,

        /// <summary>
        /// A horizontal stripe pattern.
        /// </summary>
        HorizontalStripe,

        /// <summary>
        /// A thin diagonal stripe pattern.
        /// </summary>
        ThinDiagonalStripe,

        /// <summary>
        /// A diagonal stripe pattern.
        /// </summary>
        DiagonalStripe,

        /// <summary>
        /// A thin reverse diagonal stripe pattern.
        /// </summary>
        ThinReverseDiagonalStripe,

        /// <summary>
        /// A reverse diagonal stripe pattern.
        /// </summary>
        ReverseDiagonalStripe,

        /// <summary>
        /// A thin horizontal crosshatch pattern.
        /// </summary>
        ThinHorizontalCrosshatch,

        /// <summary>
        /// A thin diagonal crosshatch pattern.
        /// </summary>
        ThinDiagonalCrosshatch,

        /// <summary>
        /// A thick diagonal crosshatch pattern.
        /// </summary>
        ThickDiagonalCrosshatch,
    }
}